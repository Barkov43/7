export function fallbackCareerResult(answers) {
  const counts = answers.reduce((acc, answer) => ({ ...acc, [answer]: (acc[answer] || 0) + 1 }), {});
  const profile = Object.entries(counts).sort((a, b) => b[1] - a[1])[0]?.[0] || 'automation';
  const variants = {
    engineer: ['Инженер-технолог', 'Вам ближе производственный процесс, материалы и контроль качества.'],
    safety: ['Специалист по промышленной безопасности', 'Ответы показывают интерес к маршрутам, рискам и безопасной организации посещений.'],
    automation: ['Инженер по автоматизации процессов', 'Вам подходит направление с датчиками, контроллерами, цифровыми линиями и анализом данных.']
  };
  return { score_profile: profile, specialty: variants[profile][0], explanation: variants[profile][1] };
}
